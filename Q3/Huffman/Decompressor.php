			$node = $this->tree;
			self::$bloc = $offset;

			while ($node && $node->symbol == Q3_HUFFMAN_INTERNAL_NODE) {
				if (self::getBit($fin, $offset)) {
					$node = $node->right;
				} else {
					$node = $node->left;
				}
			}

			if (!$node) {
				$ch = 0;
				return;
			}

			$ch = $node->symbol;
			$offset = self::$bloc;

			return $ch;
		}

		public static function GetBit(&$fin, &$offset) {
			self::$bloc = $offset;
			$t = (ord($fin{(self::$bloc>>3)}) >> (self::$bloc&7)) & 0x1;
			self::$bloc++;
			$offset = self::$bloc;
			return $t;
		}
